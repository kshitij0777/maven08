package loops;

public class WhileLoopStudyEX {

	public static void main(String[] args) 
	{
	
	      //  i want table 7
		     //start---->7 condition------> >=70 updation---->  i=i+7
			int k=7;
			while(k<=70)
			{
				System.out.println(k);
				k=k+7;
			}
			
			 //  i want table 7 reverse
		     //start---->70 condition------> >=7 updation---->  i=i-7
			
			int l=70;
			while(l>=7)
			{
				System.out.println(l);
				
				l=l-7;
			}
			

	}

}
