package loops;

public class WhieLoopStudy {

	
	public static void main(String[] args) 
	{
		//  i want print pune 20 time
		//start---->1 condition------> <=20 updation----i++
		
		int i =1;
		
		while(i<=20)
		{ 
			System.out.println("pune");
			i++;
	    }
		
		System.out.println("====================================================================");
		
		////  i want print pune 15time
		//start---->15 condition------> >=1 updation---->  i--
		
		int j=15;
		
		while(j>=1)
		{
			System.out.println("katraj");
			j--;	
		}
	
	}

}
